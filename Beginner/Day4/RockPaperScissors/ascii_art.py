
def represent_choice(choice):
    rock = '''
        _______
    ---'   ____)
        (_____)
        (_____)
        (____)
    ---.__(___)
    '''

    paper = '''
        _______
    ---'   ____)____
            ______)
            _______)
            _______)
    ---.__________)
    '''

    scissors = '''
        _______
    ---'   ____)____
            ______)
        __________)
        (____)
    ---.__(___)
    '''

    if choice == 'ROCK':
        output = rock
    elif choice == 'PAPER':
        output = paper
    elif choice == "SCISSORS":
        output = scissors

    return output

